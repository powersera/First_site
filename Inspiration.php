            повстання йоркців, але Генріх відновив порядок після воєн Червоної і Білої троянди і отримав незалежність від парламенту,
            заклав основи абсолютизму. Його наступником став син Генріх VIII.
			З народження до вступу на престол майбутній король мав ім'я Генріх Тюдор, граф Річмонд (Earl of Richmond). 
			За лінією батька належав до стародавнього уельського роду, що прийняв прізвище Тюдорів на честь прапрадіда Генріха, 
			Тудура ап Горонві (валл. Tudur ap Goronwy). Дід Генріха, Оуен Тюдор, перебував на службі у вдови короля Генріха V 
			й матері Генріха VI французької принцеси Катерини Валуа; невідомо точно, чи був освячений таємним шлюбом їхній
			багаторічний зв'язок, від якого народилось кілька визнаних дітей. Їхній син Едмунд Тюдор, 1-й граф Річмонд, 
			одноутробний брат короля Генріха VI, ще раз поріднився з родом Ланкастерів, одружившись з Маргаритою Бофорт, 
