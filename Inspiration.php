			Генріх VII (англ. Henry VII; 29 січня 1457 — 21 квітня 1509) Король Англії у 1485–1509 роках, син Едмунда Тюдора, графа Річмонда (1430–1456 роки), і нащадок Джона Гонта. Засновник династії Тюдорів.
	        Біло-червона троянда — емблема Тюдорів
            Провів юнацькі роки в Бретані, у 1485 висадився в Британії, щоб очолити повстання проти Річарда ІІІ, що завершилося поразкою Річарда 
